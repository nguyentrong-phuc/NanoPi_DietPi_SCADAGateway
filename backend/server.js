app.get('/api/network/routing', (req, res) => {
  let routes = [];
  if (isLinux) {
    try {
      const output = execSync('ip route').toString().trim();
      const lines = output.split('\n');
      routes = lines.map(line => {
        const parts = line.trim().split(/\s+/);
        // Default format: default via 192.168.1.1 dev eth0 src 192.168.1.10 metric 202
        // Subnet format: 192.168.1.0/24 dev eth0 proto kernel scope link src 192.168.1.10 metric 202
        let target = parts[0];
        let gateway = '0.0.0.0';
        let netmask = '255.255.255.255';
        let flags = 'U';
        let iface = '';
        let metric = '0';
        
        if (target === 'default') {
          target = '0.0.0.0';
          netmask = '0.0.0.0';
          flags = 'UG';
        } else if (target.includes('/')) {
          const [ip, cidr] = target.split('/');
          target = ip;
          // approximate netmask from cidr
          const maskBits = parseInt(cidr, 10);
          const mask = ~((1 << (32 - maskBits)) - 1) >>> 0;
          netmask = [ (mask >>> 24) & 255, (mask >>> 16) & 255, (mask >>> 8) & 255, mask & 255 ].join('.');
        }

        const viaIdx = parts.indexOf('via');
        if (viaIdx !== -1) gateway = parts[viaIdx + 1];

        const devIdx = parts.indexOf('dev');
        if (devIdx !== -1) iface = parts[devIdx + 1];

        const metricIdx = parts.indexOf('metric');
        if (metricIdx !== -1) metric = parts[metricIdx + 1];

        return {
          target,
          gateway,
          netmask,
          flag: flags,
          metric,
          ref: '0',
          use: '0',
          interface: iface
        };
      });
    } catch(e) {
      console.error("Error reading routing table:", e);
    }
  } else {
    // Mock for Windows dev
    routes = [
      { target: '0.0.0.0', gateway: '192.168.1.1', netmask: '0.0.0.0', flag: 'UG', metric: '0', ref: '0', use: '0', interface: 'eth0' },
      { target: '192.168.1.0', gateway: '0.0.0.0', netmask: '255.255.255.0', flag: 'U', metric: '0', ref: '0', use: '0', interface: 'eth0' },
      { target: '192.168.30.0', gateway: '0.0.0.0', netmask: '255.255.255.0', flag: 'U', metric: '0', ref: '0', use: '0', interface: 'eth1' }
    ];
  }
  res.json({ routes });
});

// Network Diagnostics (Ping & Traceroute)
app.post('/api/network/diagnostics', (req, res) => {
  const { target, type } = req.body;
  if (!target || !type) {
    return res.status(400).json({ error: 'Missing target or type' });
  }

  // Basic sanitization: only allow safe IP/domain characters
  if (!/^[a-zA-Z0-9._\-]+$/.test(target)) {
    return res.status(400).json({ error: 'Invalid target format' });
  }

  let cmd = '';
  if (type === 'Ping') {
    cmd = `ping -c 4 ${target}`;
  } else if (type === 'Traceroute') {
    // Try traceroute first, fall back to tracepath (pre-installed on Debian/DietPi)
    cmd = `traceroute ${target} 2>/dev/null || tracepath -n ${target}`;
  } else {
    return res.status(400).json({ error: 'Unknown diagnostic type' });
  }

  try {
    let output;
    if (isLinux) {
      output = execSync(cmd, { timeout: 30000 }).toString();
    } else {
      // Windows fallback for dev
      output = type === 'Ping'
        ? execSync(`ping ${target}`, { timeout: 15000 }).toString()
        : execSync(`tracert ${target}`, { timeout: 30000 }).toString();
    }
    res.json({ output });
  } catch (err) {
    // execSync throws on non-zero exit but still has stdout
    const output = (err.stdout && err.stdout.toString()) || err.message;
    res.json({ output });
  }
});

