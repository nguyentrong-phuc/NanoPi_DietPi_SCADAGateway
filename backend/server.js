// Global state to store previous CPU stat for accurate percentage calculation over time
let lastCpuStat = null;

app.get('/api/system/info', (req, res) => {
  const nets = os.networkInterfaces();
  const config = getNetworkConfig();
  const wanConfig = config.wan;
  const lanConfig = config.lan;
  const wlanConfig = config.wlan;

  const result = {
    name: os.hostname(),
    model: 'NanoPi R2S',
    version: '2.0.0',
    os: 'Linux (DietPi)',
    sn: '--',
    mac1: '--',
    mac2: '--',
    deviceTime: new Date().toLocaleString('sv').replace('T', ' '),
    operationTime: '--',
    cpu: 0,
    memory: 0,
    flash: 0,
    wan: wanConfig,
    lan: lanConfig,
    wlan: wlanConfig,
    eth0Status: 'Disconnected',
    eth1Status: 'Disconnected',
    wlan0Status: 'Disconnected'
  };

  // MAC addresses
  const getMac = (iface) => {
    let mac = '--';
    const n = nets[iface];
    if (n && n.length > 0) {
      mac = n[0].mac;
    }
    if ((!mac || mac === '--') && isLinux) {
      try { mac = fs.readFileSync(`/sys/class/net/${iface}/address`, 'utf-8').trim(); } catch(e) {}
    }
    return mac !== '--' ? mac.toUpperCase() : '--';
  };
  result.mac1 = getMac('eth0');
  result.mac2 = getMac('eth1');

  // Interface status
  if (nets['eth0']) {
    const ipv4 = nets['eth0'].find(n => n.family === 'IPv4' || n.family === 4);
    if (ipv4) {
      wanConfig.status = 'Connected';
      wanConfig.liveIp = ipv4.address;
      wanConfig.netmask = ipv4.netmask;
      wanConfig.mac = ipv4.mac;
    }
  }
  if (nets['eth1']) {
    const ipv4 = nets['eth1'].find(n => n.family === 'IPv4' || n.family === 4);
    if (ipv4) {
      lanConfig.status = 'Connected';
      lanConfig.liveIp = ipv4.address;
      lanConfig.netmask = ipv4.netmask;
      lanConfig.mac = ipv4.mac;
    }
  }
  if (nets['wlan0']) {
    const ipv4 = nets['wlan0'].find(n => n.family === 'IPv4' || n.family === 4);
    if (ipv4) {
      wlanConfig.status = 'Connected';
      wlanConfig.liveIp = ipv4.address;
      wlanConfig.netmask = ipv4.netmask;
      wlanConfig.mac = ipv4.mac;
    }
  }

  result.eth0Status = wanConfig.status;
  result.eth1Status = lanConfig.status;
  result.wlan0Status = wlanConfig.status;

  if (isLinux) {
    // CPU usage (sample /proc/stat compared to last API call)
    try {
      const currentStat = fs.readFileSync('/proc/stat', 'utf-8').split('\n')[0].trim().split(/\s+/).slice(1).map(Number);
      if (lastCpuStat) {
        const total1 = lastCpuStat.reduce((a, b) => a + b, 0);
        const idle1 = lastCpuStat[3];
        const total2 = currentStat.reduce((a, b) => a + b, 0);
        const idle2 = currentStat[3];
        const cpuUsage = Math.round(((total2 - total1 - (idle2 - idle1)) / Math.max(total2 - total1, 1)) * 100);
        result.cpu = Math.min(100, Math.max(0, cpuUsage));
      }
      lastCpuStat = currentStat;
    } catch(e) {}

    // Memory usage
    try {
      const meminfo = fs.readFileSync('/proc/meminfo', 'utf-8');
      const getMemVal = (key) => { const m = meminfo.match(new RegExp(`^${key}:\\s+(\\d+)`, 'm')); return m ? parseInt(m[1]) : 0; };
      const total = getMemVal('MemTotal');
      const available = getMemVal('MemAvailable');
      if (total > 0) result.memory = Math.round(((total - available) / total) * 100);
    } catch(e) {}

    // Flash usage (root partition)
    try {
      const df = execSync("df / --output=pcent | tail -1").toString().trim().replace('%', '');
      result.flash = parseInt(df) || 0;
    } catch(e) {}

    // Uptime (operation time)
    try {
      const upSec = parseFloat(fs.readFileSync('/proc/uptime', 'utf-8').split(' ')[0]);
      const h = Math.floor(upSec / 3600).toString().padStart(2, '0');
      const m = Math.floor((upSec % 3600) / 60).toString().padStart(2, '0');
      const s = Math.floor(upSec % 60).toString().padStart(2, '0');
      result.operationTime = `${h}:${m}:${s}`;
    } catch(e) {}

    // Hostname as device name
    try { result.name = execSync('hostname').toString().trim(); } catch(e) {}
  }

  res.json(result);
});

// Network Diagnostics API (Ping / Traceroute)
app.post('/api/network/diagnostics', (req, res) => {
  const { target, type } = req.body;
  if (!target || !['Ping', 'Traceroute'].includes(type)) return res.status(400).json({ error: 'Invalid parameters' });

  // Basic sanitization to prevent command injection
  if (!/^[a-zA-Z0-9.-]+$/.test(target)) return res.status(400).json({ error: 'Invalid target format' });

  let cmd = '';
  if (isLinux) {
    cmd = type === 'Ping' ? `ping -c 4 -W 2 ${target}` : `traceroute -m 20 -w 2 ${target}`;
  } else {
    cmd = type === 'Ping' ? `ping -n 4 -w 2000 ${target}` : `tracert -h 20 -w 2000 ${target}`;
  }

  require('child_process').exec(cmd, { timeout: 35000 }, (error, stdout, stderr) => {
    // We return stdout if available, otherwise stderr, otherwise error message
    res.json({ output: stdout || stderr || (error ? error.message : 'No output') });
  });
});

