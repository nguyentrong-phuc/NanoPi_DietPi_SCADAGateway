app.post('/api/network', (req, res) => {
  const { wan, lan } = req.body;
  const currentConfig = getNetworkConfig();
  const wanToSave = wan || currentConfig.wan;
  const lanToSave = lan || currentConfig.lan;
  
  try {
    // 1. Generate /etc/network/interfaces content
    let interfacesContent = `# Auto-generated by SCADA Gateway\nauto lo\niface lo inet loopback\n\n`;
    
    // WAN
    if (wanToSave) {
      interfacesContent += `auto eth0\n`;
      if (wanToSave.mode === 'DHCP') {
        interfacesContent += `iface eth0 inet dhcp\n`;
      } else {
        interfacesContent += `iface eth0 inet static\n`;
        interfacesContent += `  address ${wanToSave.staticIp}\n`;
        interfacesContent += `  netmask ${wanToSave.netmask}\n`;
        if (wanToSave.gateway) interfacesContent += `  gateway ${wanToSave.gateway}\n`;
        if (wanToSave.dnsMode === 'Manual' && (wanToSave.dns1 || wanToSave.dns2)) {
           let dnsLine = `  dns-nameservers`;
           if (wanToSave.dns1) dnsLine += ` ${wanToSave.dns1}`;
           if (wanToSave.dns2) dnsLine += ` ${wanToSave.dns2}`;
           interfacesContent += dnsLine + `\n`;
        }
      }
      if (wanToSave.mtu) interfacesContent += `  mtu ${wanToSave.mtu}\n`;
      interfacesContent += `\n`;
    }

    // LAN
    if (lanToSave) {
      interfacesContent += `auto eth1\n`;
      interfacesContent += `iface eth1 inet static\n`;
      interfacesContent += `  address ${lanToSave.ip}\n`;
      interfacesContent += `  netmask ${lanToSave.netmask}\n\n`;
      interfacesContent += `# Fallback Virtual IP for Rescue\n`;
      interfacesContent += `auto eth1:0\n`;
      interfacesContent += `iface eth1:0 inet static\n`;
      interfacesContent += `  address 10.10.10.254\n`;
      interfacesContent += `  netmask 255.255.255.0\n\n`;
    }

    // WLAN
    const wlanToSave = req.body.wlan || currentConfig.wlan;
      if (wlanToSave && wlanToSave.enabled) {
        interfacesContent += `auto wlan0\niface wlan0 inet ${wlanToSave.mode === 'Static IP' ? 'static' : 'dhcp'}\n`;
        if (wlanToSave.mode === 'Static IP') {
          if (wlanToSave.staticIp) interfacesContent += `  address ${wlanToSave.staticIp}\n`;
          if (wlanToSave.netmask) interfacesContent += `  netmask ${wlanToSave.netmask}\n`;
          if (wlanToSave.gateway) interfacesContent += `  gateway ${wlanToSave.gateway}\n`;
        }
        interfacesContent += `  wpa-conf /etc/wpa_supplicant/wpa_supplicant.conf\n\n`;
      }
      // Update wpa_supplicant.conf
      let wpaConf = `ctrl_interface=DIR=/var/run/wpa_supplicant GROUP=netdev\nupdate_config=1\ncountry=US\n\n`;
      if (wlanToSave.ssid) {
        wpaConf += `network={\n  ssid="${wlanToSave.ssid}"\n`;
        if (wlanToSave.password) {
          wpaConf += `  psk="${wlanToSave.password}"\n`;
        } else {
          wpaConf += `  key_mgmt=NONE\n`;
        }
        wpaConf += `}\n`;
      }
      fs.writeFileSync(wpaSupplicantPath, wpaConf);

    fs.writeFileSync(interfacesPath, interfacesContent);

    // 2. Generate dnsmasq config for LAN DHCP
    if (lanToSave) {
      let dnsmasqContent = '';
      if (lanToSave.dhcpEnabled) {
        dnsmasqContent += `interface=eth1\n`;
        dnsmasqContent += `dhcp-range=eth1,${lanToSave.dhcpStart},${lanToSave.dhcpEnd},${lanToSave.netmask},${lanToSave.leaseTime}m\n`;
        if (lanToSave.dns) {
          dnsmasqContent += `dhcp-option=eth1,6,${lanToSave.dns}\n`;
        }
        if (lanToSave.staticHosts && lanToSave.staticHosts.length > 0) {
          lanToSave.staticHosts.forEach(host => {
            if (host.mac && host.ip) {
              const hostname = host.hostname || '*';
              dnsmasqContent += `dhcp-host=${host.mac},${host.ip},${hostname}\n`;
            }
          });
        }
      }
      fs.writeFileSync(dnsmasqPath, dnsmasqContent);
    }

    res.json({ success: true, message: 'Network configuration updated successfully!' });

    // 3. Restart services asynchronously so the response reaches the client first
    if (isLinux) {
      setTimeout(() => {
        console.log('Restarting networking and dnsmasq...');
        try { execSync('systemctl restart dnsmasq'); } catch(e) {}
        try { execSync('ifdown -a'); } catch(e) {}
        try { execSync('killall wpa_supplicant'); } catch(e) {}
        try { execSync('rm -rf /var/run/wpa_supplicant/wlan0'); } catch(e) {}
        try { execSync('rfkill unblock wlan'); } catch(e) {}
        try { execSync('systemctl restart networking'); } catch(e) {}
      }, 1000);
    }
  } catch (error) {
    console.error(error);
    res.status(500).json({ error: 'Failed to apply network configuration', details: error.message });
  }
});

