// Network API (WAN: eth0, LAN: eth1)
const getNetworkConfig = () => {
  const nets = os.networkInterfaces();
  
  let wanConfig = {
    mode: 'DHCP', dnsMode: 'Auto', dns1: '8.8.8.8', dns2: '8.8.4.4', mtu: '1500',
    staticIp: '', netmask: '255.255.255.0', gateway: '', mac: '', status: 'Disconnected'
  };
  let lanConfig = {
    ip: '170.0.0.1',
    netmask: '255.255.255.0',
    dhcpEnabled: false,
    dhcpStart: '',
    dhcpEnd: '',
    leaseTime: 1440,
    dns: '8.8.8.8',
    staticHosts: [],
    dhcpHosts: []
  };
  let wlanConfig = {
    enabled: false, mode: 'DHCP', ssid: '', password: '', staticIp: '', netmask: '255.255.255.0', gateway: '', mac: '', status: 'Disconnected'
  };

  // 1. Parse /etc/network/interfaces
  try {
    if (fs.existsSync(interfacesPath)) {
      const ifaces = fs.readFileSync(interfacesPath, 'utf-8');
      
      const eth0BlockMatch = ifaces.match(/iface eth0 inet static[\s\S]*?(?=iface|$)/);
      if (eth0BlockMatch) {
        wanConfig.mode = 'Static IP';
        wanConfig.dnsMode = 'Manual';
        const block = eth0BlockMatch[0];
        const getVal = (key) => { const m = block.match(new RegExp(`^\\s*${key}\\s+(.+)`, 'm')); return m ? m[1].trim() : ''; };
        wanConfig.staticIp = getVal('address');
        wanConfig.netmask = getVal('netmask') || '255.255.255.0';
        wanConfig.gateway = getVal('gateway');
        wanConfig.mtu = getVal('mtu') || '1500';
        const dns = getVal('dns-nameservers');
        if (dns) {
          const dnsParts = dns.split(/\s+/);
          if(dnsParts[0]) wanConfig.dns1 = dnsParts[0];
          if(dnsParts[1]) wanConfig.dns2 = dnsParts[1];
        }
      }

      const eth1BlockMatch = ifaces.match(/iface eth1 inet static[\s\S]*?(?=iface|$)/);
      if (eth1BlockMatch) {
        const block = eth1BlockMatch[0];
        const getVal = (key) => { const m = block.match(new RegExp(`^\\s*${key}\\s+(.+)`, 'm')); return m ? m[1].trim() : ''; };
        lanConfig.ip = getVal('address') || '170.0.0.1';
        lanConfig.netmask = getVal('netmask') || '255.255.255.0';
      }

      const wlan0BlockMatch = ifaces.match(/iface wlan0 inet static[\s\S]*?(?=iface|$)/);
      if (wlan0BlockMatch) {
        wlanConfig.mode = 'Static IP';
        const block = wlan0BlockMatch[0];
        const getVal = (key) => { const m = block.match(new RegExp(`^\\s*${key}\\s+(.+)`, 'm')); return m ? m[1].trim() : ''; };
        wlanConfig.staticIp = getVal('address');
        wlanConfig.netmask = getVal('netmask') || '255.255.255.0';
        wlanConfig.gateway = getVal('gateway');
      }
      if (ifaces.includes('iface wlan0 inet')) {
        wlanConfig.enabled = true;
      }
    }
  } catch(e) { console.error("Error reading interfaces:", e); }

  // Parse wpa_supplicant.conf
  try {
    if (fs.existsSync(wpaSupplicantPath)) {
      const wpaConf = fs.readFileSync(wpaSupplicantPath, 'utf-8');
      const ssidMatch = wpaConf.match(/ssid="([^"]+)"/);
      const pskMatch = wpaConf.match(/psk="([^"]+)"/);
      if (ssidMatch) wlanConfig.ssid = ssidMatch[1];
      if (pskMatch) wlanConfig.password = pskMatch[1];
    }
  } catch(e) { console.error("Error reading wpa_supplicant:", e); }

  // 2. Parse dnsmasq config for LAN DHCP
  try {
    if (fs.existsSync(dnsmasqPath)) {
      const dnsmasqContent = fs.readFileSync(dnsmasqPath, 'utf-8');
      if (dnsmasqContent.includes('interface=eth1')) lanConfig.dhcpEnabled = true;
      const lines = dnsmasqContent.split('\n');
      lines.forEach(line => {
        if (line.trim().startsWith('dhcp-range=eth1')) {
          const parts = line.split('=')[1].split(',');
          lanConfig.dhcpStart = parts[1] || '';
          lanConfig.dhcpEnd = parts[2] || '';
        }
        if (line.trim().startsWith('dhcp-option=eth1,6,')) {
          lanConfig.dns = line.split(',').pop().trim();
        }
        if (line.trim().startsWith('dhcp-host=')) {
          const parts = line.split('=')[1].split(',');
          if (parts.length >= 2) {
            lanConfig.staticHosts.push({
              mac: parts[0] || '',
              ip: parts[1] || '',
              hostname: parts[2] || ''
            });
          }
        }
      });
    }
    
    // Parse dnsmasq.leases for dynamic hosts
    const leasesPath = '/var/lib/misc/dnsmasq.leases';
    if (fs.existsSync(leasesPath)) {
      const leasesContent = fs.readFileSync(leasesPath, 'utf-8');
      leasesContent.split('\n').forEach(line => {
        const parts = line.trim().split(/\s+/);
        if (parts.length >= 4) {
          const expiresAt = parseInt(parts[0]) * 1000; // js uses ms
          lanConfig.dhcpHosts.push({
            mac: parts[1],
            ip: parts[2],
            hostname: parts[3] === '*' ? '--' : parts[3],
            leaseTime: isNaN(expiresAt) ? '--' : new Date(expiresAt).toLocaleString()
          });
        }
      });
    }

  } catch (e) { console.error("Error reading dnsmasq:", e); }

  // 3. Map live network interfaces (MAC and Live IP)
  const mapLive = (ethName, conf) => {
    if (nets[ethName] && nets[ethName].length > 0) {
      conf.mac = nets[ethName][0].mac;
      const ipv4 = nets[ethName].find(n => n.family === 'IPv4' || n.family === 4);
      if (ipv4) {
        conf.liveIp = ipv4.address;
        conf.liveNetmask = ipv4.netmask;
        conf.status = 'Connected';
      }
    }
    // Fallback: Read MAC from sysfs if interface is down and missing from os.networkInterfaces
    if ((!conf.mac || conf.mac === '--') && isLinux) {
      try {
        const mac = fs.readFileSync(`/sys/class/net/${ethName}/address`, 'utf-8').trim();
        if (mac) conf.mac = mac;
      } catch(e) {}
    }
  };
  mapLive('eth0', wanConfig);
  mapLive('eth1', lanConfig);
  mapLive('wlan0', wlanConfig);

  // 4. Try getting live gateway for WAN
  if (isLinux) {
    try {
      const gw = execSync("ip route show default dev eth0 | awk '{print $3}'").toString().trim();
      if (gw) wanConfig.liveGateway = gw;
    } catch(e) {}
  }

  // 5. Read /proc/net/dev for RX/TX bytes and connection uptime
  const formatBytes = (bytes) => {
    const b = parseInt(bytes) || 0;
    if (b >= 1024 * 1024) return `${(b / 1024 / 1024).toFixed(1)} MB`;
    if (b >= 1024) return `${(b / 1024).toFixed(1)} KB`;
    return `${b} B`;
  };

  const getConnTime = (iface) => {
    // Reads carrier_up_count + operstate to estimate time online
    // Fallback: use system uptime as max bound
    if (!isLinux) return '--';
    try {
      const state = fs.readFileSync(`/sys/class/net/${iface}/operstate`, 'utf-8').trim();
      if (state !== 'up') return 'Disconnected';
      // Read uptime in seconds
      const uptime = parseFloat(fs.readFileSync('/proc/uptime', 'utf-8').split(' ')[0]);
      const h = Math.floor(uptime / 3600).toString().padStart(2, '0');
      const m = Math.floor((uptime % 3600) / 60).toString().padStart(2, '0');
      const s = Math.floor(uptime % 60).toString().padStart(2, '0');
      return `${h}:${m}:${s}`;
    } catch(e) { return '--'; }
  };

  if (isLinux) {
    try {
      const netDev = fs.readFileSync('/proc/net/dev', 'utf-8');
      netDev.split('\n').forEach(line => {
        const trimmed = line.trim();
        if (trimmed.startsWith('eth0:')) {
          const parts = trimmed.replace('eth0:', '').trim().split(/\s+/);
          wanConfig.receive = formatBytes(parts[0]);  // RX bytes
          wanConfig.send = formatBytes(parts[8]);     // TX bytes
        }
        if (trimmed.startsWith('eth1:')) {
          const parts = trimmed.replace('eth1:', '').trim().split(/\s+/);
          lanConfig.receive = formatBytes(parts[0]);
          lanConfig.send = formatBytes(parts[8]);
        }
        if (trimmed.startsWith('wlan0:')) {
          const parts = trimmed.replace('wlan0:', '').trim().split(/\s+/);
          wlanConfig.receive = formatBytes(parts[0]);
          wlanConfig.send = formatBytes(parts[8]);
        }
      });
    } catch(e) {}
    wanConfig.connTime = getConnTime('eth0');
    lanConfig.connTime = getConnTime('eth1');
    wlanConfig.connTime = getConnTime('wlan0');
  }

  return { wan: wanConfig, lan: lanConfig, wlan: wlanConfig };
};

